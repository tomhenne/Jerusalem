package de.esymetric.jerusalem.ownDataRepresentation.geoData;

public class Coord2H
{
    public double x;
    public double y;
    public double heading;

    public Coord2H() { }

    public Coord2H(double x, double y, double heading)
    {
        this.x = x;
        this.y = y;
        this.heading = heading;
    }

}
